import argparse
import os

import numpy as np

from cnn.input import (Dataloader, Dataset, get_validation_augmentation)


def main(**args):

    patch_size = (args['image_size'], args['image_size'], args['num_channels'])
    val_augmentation = get_validation_augmentation(patch_size)
    
    val_dataset = Dataset(
        data_path=os.path.join(args['data_path'], 'train'),
        selected=None,
        only_non_empty_slices=True,
    )
    
    val_dataloader = Dataloader(
        dataset=val_dataset,
        augmentation=val_augmentation,
        shuffle=False,
    )

    data = {}
    num_images = 0
    for _, mask in val_dataloader:
        unique, _ = np.unique(mask, return_counts=True)
        key = str(np.sort(unique))
        if key in data:
            data[key] += 1
        else:
            data[key] = 1
        num_images += 1
    print(f"Train data ({num_images} images): {data} ")

    test_dataset = Dataset(
        data_path=os.path.join(args['data_path'], 'test'),
        selected=None,
        only_non_empty_slices=True,
    )
    
    test_dataloader = Dataloader(
        dataset=test_dataset,
        augmentation=val_augmentation,#only resize image
        shuffle=False,
    )

    data = {}
    num_images = 0
    for _, mask in test_dataloader:
        unique, _ = np.unique(mask, return_counts=True)
        key = str(np.sort(unique))
        if key in data:
            data[key] += 1
        else:
            data[key] = 1
        num_images += 1
    print(f"Test data ({num_images} images): {data} ")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--data_path", type=str, required=True, help="Path to experiment_path."
    )

    parser.add_argument(
        "--image_size", type=int, required=True, help="Size of the image."
    )

    parser.add_argument(
        "--num_channels", type=int, required=True, help="Channels of the image."
    )

    arguments = parser.parse_args()

    main(**vars(arguments))